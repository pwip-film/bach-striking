				<div class="about-description__text-wrapper">
					<p class="about-description__text"><span class="about-description__emphasize">Striking a Chord: Instruments of Healing</span> shows how veterans with PTSD are finding their way back to themselves through music and art. The spine of the 30-minute documentary is singer-songwriter Nell Bryden’s concert tour of U.S. military bases in Iraq. Her performances carve out a sliver of rare emotional space for soldiers whose daily routines require suppression of feelings. We see how the music transforms – in body language, facial expressions, and the intimacy of the stories shared by individual soldiers in post-concert private exchanges with the band.</p>

					<p class="about-description__text">Director Susan Rockefeller also intertwines footage from the battlefield with footage of veterans fighting a very different battle – a battle to rediscover normal. Through art, dance, theater, and music, vets gradually find their voice and their health. Their journeys offer civilians insight into the trauma of war and the challenges that returning soldiers face when they re-enter “reality.” Soldiers learn that, for those who find the courage to speak their truths, there are unexpected paths to healing.</p>
